print("----Karşılaştırma Operatörler----")

#* ==: eşit mi operatörü
#* >: büyük mü operatörü
#* <: küçük mü operatörü
#* >=: büyük veya eşit mi operatörü
#* <=: küçük veya eşit mi operatörü
#* !=: eşit değil mi operatörü 

print("4 > 2 =" , (4 > 2))
print("4 < 2 =" , (4 < 2))
print("4 <= 2 =" , (4 <= 2))
print("4 >= 2 =" , (4 >= 2))
print("4 == 2 =" , (4 == 2))
print("4 != 2 =" , (4 != 2))