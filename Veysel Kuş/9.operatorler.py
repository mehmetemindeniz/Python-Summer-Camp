print("----Aritmetik Operatörler----")
#* +: toplama operatörü
#* -: çıkarma operatörü
#* *: çarpma operatörü
#* /: bölmme operatörü
#* %: mod alma operatörü
#* **: üssü operatörü
#* //: böler ve floor uygular

print("4 + 2 =" , (4 + 2))
print("4 - 2 =" , (4 - 2))
print("4 * 2 =" , (4 * 2))
print("4 / 2 =" , (4 / 2))
print("4 % 2 =" , (4 % 2))
print("4**2 =" , (4 ** 2))
print("97 // 45 =" , (97 // 45))


print("----Atama Operatörler----")
#* =: atama operatörü
#* +=: a=a+4 => a+=4
#* -=: a=a-4 => a-=4
#* *=: a=a*4 => a*=4
#* /=: a=a/4 => a/=4
#* //=: a=a//4 => a//=4
#* %=: a=a%4 => a%=4
#* **=: a=a**4 => a**=4

sayi = 4
print("sayi = 4 =" , sayi)
sayi += 4
print("sayi += 4 =" , sayi)
sayi -= 4
print("4 -= 4 = " , sayi)
sayi *= 4
print("4 *= 4 =" , sayi)
sayi /= 4
print("4 /= 4 = " , sayi)
sayi //= 4
print("sayi //= 4 = " , sayi)
sayi %= 4
print("sayi %= 4 =" , sayi)
sayi **= 4
print("sayi **= 4 = " , sayi)


